#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	size_t	slen;
	char	*res;
	int		i;

	if (!s || !f)
		return (NULL);
	slen = ft_strlen(s);
	res = (char *)malloc((slen * sizeof(char)) + 1);
	if (!res)
		return (NULL);
	i = 0;
	while (s[i])
	{
		res[i] = (*f)(i, s[i]);
		i++;
	}
	res[i] = 0;
	return (res);
}
